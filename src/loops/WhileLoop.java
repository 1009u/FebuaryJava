package loops;

public class WhileLoop {
    public static void main(String[] args) {
        //Syntax: while (condition){code}
        int i=0;
        while (i<10){
            System.out.println("Increment of I is ...."+i);
            i++;
        }
        //table of 12

        int b=1;
        while (b<=10){
            System.out.println("Table of 12 ..."+12+"*"+b+"="+(b*12));
            b++;
        }
    }
}
